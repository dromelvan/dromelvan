player_season_infos.each do |team_id, parameterized_name, position_id|
  season = Season.current
  players = Player.where(parameterized_name: parameterized_name)
  if players.size != 1 then
    puts "#{players.size} matches for player #{parameterized_name}"
  else
    player = players.first
    team = Team.find(team_id)
    position = Position.find(position_id)
    player_season_info = PlayerSeasonInfo.where(player: player, season: season).first

    if !player_season_info.nil? then
      puts "Updating player #{player.name}, position #{position.name}, team #{team.name}"
      player_season_info.team = team
      player_season_info.position = position
      player_season_info.save
    else
      puts "Inserting player #{player.name}, position #{position.name}, team #{team.name}"
      PlayerSeasonInfo.create(player: player, season: Season.current, team: team, position: position)
    end

    if !PlayerSeasonStat.where(player: player, season: season).any? then
      PlayerSeasonStat.create(player: player, season: season)
    end

  end
end
